using namespace std;
map<ll, ll> m;
ll factor = 1;
void primefactorization(ll n)
{
    ll i, num = n;
    for (i = 2; i * i <= num; i++)
    {
        if (n % i == 0)
        {
            while (n % i == 0)
            {
                m[i]++;
                n /= i;
            }
            factor = max(factor, m[i]);
        }
    }
    // cout<<n<<ss;
    if (n != 1)
    {
        m[n]++;
        factor = max(factor, m[n]);
    }
}
