    {
        cin >> n;
        primefactorization(n);
        ll ans = 0, sum = 1;
        while (factor--)
        {
            sum = 1;
            for (auto e : m)
            {
                if (e.second > 0)
                {
                    m[e.first]--;
                    sum *= e.first; // one
                    // cout << e.first << ss << e.second << endl;
                }
            }
            ans += sum;
        }
        cout << ans << endl;
    }
